function initMap() {
  const map = new google.maps.Map(document.getElementById('map'), {
    center: verticesPath[0].position,
    zoom: 5,
    mapId: '4504f8b37365c3d0',
  });
  
  const path = [];
  
  verticesPath.forEach(vertex => {
  	path.push(vertex.position);
  
  	if (vertex.createContent) {
    	const marker = new google.maps.Marker({
      	position: vertex.position,
      	map
      });
      
      const infowindow = new google.maps.InfoWindow({
        content: vertex.createContent()
      });
      
      marker.addListener("click", () => {
        infowindow.open({
          anchor: marker,
          map
        });
      });
    }
  });
 	  
  verteciesSingle.forEach(vertex => {
  
  	if (vertex.createContent) {
    	const marker = new google.maps.Marker({
      	position: vertex.position,
      	map
      });
      
      const infowindow = new google.maps.InfoWindow({
        content: vertex.createContent()
      });
      
      marker.addListener("click", () => {
        infowindow.open({
          anchor: marker,
          map
        });
      });
    }
  });
  
  const polyTemp = new google.maps.Polyline({
    path: november1,
    strokeColor: "#0EAEEE",
    strokeOpacity: 1.0,
    strokeWeight: 2,
  });
  polyTemp.setMap(map);
  
  const polyTemp2 = new google.maps.Polyline({
    path: november1_2,
    strokeColor: "#08688E",
    strokeOpacity: 1.0,
    strokeWeight: 2,
  });
  polyTemp2.setMap(map);
  
  const poly = new google.maps.Polyline({
    path,
    strokeColor: "#FF0000",
    strokeOpacity: 1.0,
    strokeWeight: 2,
  });
  
  poly.setMap(map);
}

window.initMap = initMap;
